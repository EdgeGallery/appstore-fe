<template>
  <div
    class="bread-crumb"
    v-if="isShow"
  >
    <el-breadcrumb separator="/">
      <el-breadcrumb-item :to="{ path: '/index' }">
        {{ $t('nav.appstore') }}
      </el-breadcrumb-item>
      <el-breadcrumb-item
        v-for="item in breadCrumbData"
        :key="item.name"
        :to="item.path"
      >
        {{ language === 'en' ? item.nameEn : item.nameCn }}
      </el-breadcrumb-item>
    </el-breadcrumb>
  </div>
</template>

<script>
import { mapState } from 'vuex'
export default {
  data () {
    return {
      breadCrumbData: [],
      isShow: false
    }
  },
  computed: {
    ...mapState(['language'])
  },
  watch: {
    $route (route) {
      this.isShow = false
      this.breadCrumbData = route.meta.breadcrumb
      if (this.breadCrumbData && this.breadCrumbData.length > 0) this.isShow = true
    }
  },
  methods: {},
  mounted () {
    this.breadCrumbData = this.$route.meta.breadcrumb
    if (this.breadCrumbData && this.breadCrumbData.length > 0) this.isShow = true
  }
}
</script>
<style lang='less'>
.bread-crumb {
  padding: 0 56px;
  .el-breadcrumb {
    line-height: 70px;
    font-size: 14px;
  }
}
</style>
